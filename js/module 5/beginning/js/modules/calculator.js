// ADD FOUR PUBLIC FUNCTIONS BELOW

// ADD FUNCTION
function add(num1, num2){
    return num1 + num2;
}

// SUBTRACT FUNCTION
function sub (num1, num2){
    return num1 - num2;
}

// MULTIPLY FUNCTION
function multi (num1, num2){
    return num1 * num2;
}

// DIVIDE FUNCTION
function div (num1, num2){
    return num1 / num2;
}

// EXPORT THE FOUR PUBLIC FUNCTIONS
export {div, add, multi, sub}




